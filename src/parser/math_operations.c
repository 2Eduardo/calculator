#include "math_operations.h"

double
add (double a, double b)
{
  return a + b;
}

double
sub (double a, double b)
{
  return a - b;
}

double
mul (double a, double b)
{
  return a * b;
}

double
divide (double a, double b)
{
  return a / b;
}

double
negate (double a)
{
  return -a;
}
